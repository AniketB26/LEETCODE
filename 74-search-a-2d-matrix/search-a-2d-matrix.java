class Solution {
    public boolean searchMatrix(int[][] matrix, int target) {
        int row = matrix.length;
        int col = matrix[0].length;


        int l =0;
        int r = row*col -1;

        while(l<= r){
            int mid = l + (r-l)/2;

            int rr = mid/col;
            int cc = mid % col;

            int val = matrix[rr][cc];

            if(val == target) return true;
            else if(val < target) l = mid +1;
            else r = mid-1;
        }

        return false;
    }
}